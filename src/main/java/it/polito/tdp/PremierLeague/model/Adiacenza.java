package it.polito.tdp.PremierLeague.model;

public class Adiacenza {
	
	int m1;
	int m2;
	int peso;
	
	
	
	public Adiacenza(int m1, int m2, int peso) {
		this.m1 = m1;
		this.m2 = m2;
		this.peso = peso;
	}
	
	public int getM1() {
		return m1;
	}
	public int getM2() {
		return m2;
	}
	public int getPeso() {
		return peso;
	}
	
	

}
